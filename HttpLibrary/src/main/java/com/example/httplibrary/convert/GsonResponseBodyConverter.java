package com.example.httplibrary.convert;


import com.google.gson.Gson;
import com.google.gson.TypeAdapter;
import com.google.gson.stream.JsonReader;

import java.io.ByteArrayInputStream;
import java.io.IOException;
import java.io.InputStream;
import java.io.InputStreamReader;
import java.io.Reader;
import java.nio.charset.Charset;

import okhttp3.MediaType;
import okhttp3.ResponseBody;
import retrofit2.Converter;

import static okhttp3.internal.Util.UTF_8;


final class GsonResponseBodyConverter<T> implements Converter<ResponseBody, T> {
    private final Gson gson;
    private final TypeAdapter<T> adapter;

    GsonResponseBodyConverter(Gson gson, TypeAdapter<T> adapter) {
        this.gson = gson;
        this.adapter = adapter;
    }

    @Override
    public T convert(ResponseBody value) throws IOException {
        String response = value.string();
        /*BaseData baseData = gson.fromJson(response, BaseData.class);
        if (baseData.code.equals(Constants.CODE_NO_LOGIN) || baseData.code.equals(Constants.CODE_NOT_SAME_DEVICE)) {
            value.close();
            throw new ApiException(baseData.code, baseData.msg);
        }*/
        /*if (AntApplication.getInstance().getString(R.string.tips_not_login).equals(baseData.msg) || AntApplication.getInstance().getString(R.string.tips_not_same_device).equals(baseData.msg)) {
            value.close();
            throw new ApiException(baseData.code, baseData.msg);
        }*/

        MediaType contentType = value.contentType();
        Charset charset = contentType != null ? contentType.charset(UTF_8) : UTF_8;
        InputStream inputStream = new ByteArrayInputStream(response.getBytes());
        Reader reader = new InputStreamReader(inputStream, charset);
        JsonReader jsonReader = gson.newJsonReader(reader);

        try {
            return adapter.read(jsonReader);
        } finally {
            value.close();
        }
    }
}
